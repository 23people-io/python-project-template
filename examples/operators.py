# Operators example in Python


def main():
    addition = 1 + 2
    substraction = 10 - 2
    mult = 10 * 3

    division = 10 / 3
    floor_division = 10 // 3
    mod = 10 % 3

    exponential = 5**3

    print(f"Addition between 1 and 2: {addition}")
    print(f"Substraction between 10 and 2: {substraction}")
    print(f"Multiplication between 10 and 3: {mult}")

    print(f"Division between 10 and 3: {division}")
    print(f"Floor division between 10 and 3: {floor_division}")
    print(f"Modulus between 10 and 3: {mod}")

    print(f"Exponential between 5 and 3: {exponential}")


if __name__ == "__main__":
    main()
